package basicPrograms;
//A Java program to print primitive data types value in java. 
public class DataTypesExample {
	public static void main(String[] args) {
		System.out.println("Main starts");
		
		System.out.println("=============================");
		byte byteData = 125;
		short shortData = 127;
		int intData = 128976;
		long longData = 12343675;
		float floatData = 12398.876567f;
		double doubleData = 6543.876543;
		char charData = 'a';
		boolean boolData = true;
		
		System.out.println("We have 8 primitive data types in Java: ");
		System.out.println("=============================");
		System.out.println("Byte Value : "+byteData);
		System.out.println("Short Value : "+shortData);
		System.out.println("Integer Value : "+intData);
		System.out.println("Long Value : "+longData);
		System.out.println("Float Value : "+floatData);
		System.out.println("Double Value : "+doubleData);
		System.out.println("Charecter Value : "+charData);
		System.out.println("Boolean Value : "+boolData);
		System.out.println("=============================");
		System.out.println("Main Ends");
	}

}
